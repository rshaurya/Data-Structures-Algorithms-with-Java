package Java_Basics;
// exploring different data types in java

public class data_types {
    public static void main(String[] args) {
        byte b = 8; 
        System.out.println(b);

        char ch = 'a'; // can store only 1 character value
        System.out.println(ch);

        boolean var = true; 
        System.out.println(var);

        double price = 10.5; // double can store larger values than float
        System.out.println(price);


    }
}
